document.addEventListener("DOMContentLoaded", () => {
    const urlParams = new URLSearchParams(window.location.search);
    const projectId = urlParams.get("project");

    if (projectId && projects[projectId]) {
        const project = projects[projectId];
        document.getElementById("project-title").innerText = `Project: ${project.title}`;
        document.getElementById("project-link").href = project.link;

        const projectImages = document.getElementById("project-images");
        project.images.forEach(img => {
            const imageElement = document.createElement("img");
            imageElement.src = img;
            imageElement.alt = "Project Image";
            projectImages.appendChild(imageElement);
        });

        const projectDetails = document.getElementById("project-details");

        project.stages.forEach(stage => {
            const stageElement = document.createElement("div");
            stageElement.classList.add("project-stage");

            const stageImageContainer = document.createElement("div");
            stageImageContainer.classList.add("stage-image-container");
            const stageImage = document.createElement("img");
            stageImage.src = stage.image;
            stageImage.alt = `${stage.title} Image`;
            stageImage.classList.add("stage-image");
            stageImageContainer.appendChild(stageImage);

            const stageContent = document.createElement("div");
            stageContent.classList.add("stage-content");

            const stageIcon = document.createElement("i");
            stageIcon.className = stage.icon;
            stageContent.appendChild(stageIcon);

            const stageTitle = document.createElement("h3");
            stageTitle.innerText = stage.title;
            stageContent.appendChild(stageTitle);

            const stageDescription = document.createElement("p");
            stageDescription.innerText = stage.description;
            stageContent.appendChild(stageDescription);

            const stageProblem = document.createElement("h4");
            stageProblem.innerText = "Problem:";
            stageContent.appendChild(stageProblem);

            const stageProblemDescription = document.createElement("p");
            stageProblemDescription.innerText = stage.problem;
            stageContent.appendChild(stageProblemDescription);

            const stageSolution = document.createElement("h4");
            stageSolution.innerText = "Solution:";
            stageContent.appendChild(stageSolution);

            const stageSolutionDescription = document.createElement("p");
            stageSolutionDescription.innerText = stage.solution;
            stageContent.appendChild(stageSolutionDescription);

            stageElement.appendChild(stageImageContainer);
            stageElement.appendChild(stageContent);

            projectDetails.appendChild(stageElement);
        });

        const conclusionElement = document.createElement("h2");
        conclusionElement.innerText = "Conclusion";
        projectDetails.appendChild(conclusionElement);

        const conclusionDescription = document.createElement("p");
        conclusionDescription.innerText = project.conclusion;
        projectDetails.appendChild(conclusionDescription);
    } else {
        document.getElementById("project-title").innerText = "Project Not Found";
        document.getElementById("project-details").innerText = "The project you are looking for does not exist.";
    }
});